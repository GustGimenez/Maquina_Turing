    private Gramatica verificaEntrada() {
        String aux, aux2;
        int auxLenght;
        ArrayList<String> strAux = new ArrayList();
        for (int i = 0; i < this.jTableGram.getModel().getRowCount(); i++) {
            aux = (String) this.jTableGram.getModel().getValueAt(i, 0);
            aux2 = (String) this.jTableGram.getModel().getValueAt(i, 2);

            if (aux == null || aux.isEmpty()) {
                if (aux2 == null || aux2.isEmpty()) {
                    continue;
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Parte NÃO-TERMINAL não pode ser vazia! Linha " + i + 1);
                    return null;
                }

            }
            if (aux.length() > 1) {
                JOptionPane.showMessageDialog(rootPane, "Parte NÃO-TERMINAL não pode conter mais de um elemento! Linha " + i + 1);
                return null;
            }

            if (aux2 == null || aux2.isEmpty()) {
                this.jTableGram.getModel().setValueAt("" + '\u03bb', i, 2);
                aux2 = "" + '\u03bb';

            }

            auxLenght = aux2.length();
            if (!Character.isUpperCase(aux.charAt(0))) {
                JOptionPane.showMessageDialog(rootPane, "Erros na sintaxe da parte NÃO-TERMINAL! Linha " + i + 1);
                return null;
            }
            if (auxLenght > 2) {
                JOptionPane.showMessageDialog(rootPane, "Gramática deve ser REGULAR! Linha " + i + 1);
                return null;
            }
            if (Character.isLowerCase(aux2.charAt(0)) || Character.isDigit(aux2.charAt(0))) {
                if (auxLenght == 2 && (!Character.isUpperCase(aux2.charAt(1)) || aux2.charAt(0) == '\u03bb')) {
                    JOptionPane.showMessageDialog(rootPane, "Gramática deve ser REGULAR UNITÁRIA A ESQUERDA! Linha " + i + 1);
                    return null;
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Gramática deve ser REGULAR UNITÁRIA A ESQUERDA! Linha " + i + 1);
                return null;
            }

            strAux.add(aux);
            strAux.add(aux2);
        }
        return new Gramatica(strAux);
    }

