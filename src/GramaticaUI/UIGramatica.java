    private void jButtonInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInputActionPerformed
        CellEditor ce = this.jTableGram.getCellEditor();
        if (ce != null) {
            ce.stopCellEditing();
        }
        Gramatica g = this.verificaEntrada();
        if (g == null) {
            return;
        }
        String str = JOptionPane.showInputDialog(rootPane, "Insira uma Entrada:");
        if (str == null) {
            return;
        }

        if (g.busca(str)) {
            JOptionPane.showMessageDialog(this, "Aceito");
        } else {
            JOptionPane.showMessageDialog(this, "Rejeitado");
        }

    }//GEN-LAST:event_jButtonInputActionPerformed

